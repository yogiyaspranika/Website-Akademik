                <p>E-learning adalah suatu sistem atau konsep pendidikan yang memanfaatkan teknologi informasi 
                dalam proses belajar mengajar. Berikut beberapa pengertian E-learning dari berbagai sumber:</p>

                <li style="margin-left:30px;">Pembelajaran yang disusun dengan tujuan menggunakan sistem elektronik atau komputer sehingga 
                mampu mendukung proses pembelajaran (Michael, 2013:27).</li>
                <li style="margin-left:30px;">Proses pembelajaran jarak jauh dengan menggabungkan 
                prinsip-prinsip dalam proses pembelajaran dengan teknologi (Chandrawati, 2010).</li>
                <li style="margin-left:30px;">Sistem pembelajaran yang digunakan sebagai sarana untuk proses belajar mengajar yang 
                dilaksanakan tanpa harus bertatap muka secara langsung antara guru dengan siswa (Ardiansyah, 2013).</li>
                
                <p>Menurut Rosenberg (2001) karakteristik E-learning bersifat jaringan, yang membuatnya mampu memperbaiki secara cepat, 
                menyimpan atau memunculkan kembali, mendistribusikan, dan sharing pembelajaran dan informasi. Kelebihan E-learning  ialah memberikan fleksibilitas, interaktivitas, 
                kecepatan, visualisasi melalui berbagai kelebihan dari masing-masing media (Sujana, 2005 : 253 ). </p>

